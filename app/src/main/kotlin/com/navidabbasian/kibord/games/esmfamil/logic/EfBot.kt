        letter: String,
        level: EfBotLevel,
        random: Random = Random.Default,
    ): Map<String, String> = topics.associateWith { topic ->
        val candidates = BANK[topic.trim()]
            ?.filter { EfScoring.startsWithLetter(it, letter) }
            .orEmpty()
        if (candidates.isNotEmpty() && random.nextDouble() < level.knowProb)
            candidates.random(random)
        else ""
    }

