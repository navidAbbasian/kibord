/**
 * سطح هوش ربات:
 * @param title برچسب فارسی برای رابط
 * @param knowProb احتمال بلدبودن جواب برای موضوعی که در بانک هست
 * @param stopFraction اگر همه را پر کرده باشد، در این کسر از زمانِ راند استپ می‌زند — تهی یعنی هرگز
 */
enum class EfBotLevel(val title: String, val knowProb: Double, val stopFraction: Double?) {
    EASY("آسون 🐣", 0.45, null),
    NORMAL("معمولی 🤖", 0.70, 0.80),
    HARD("حرفه‌ای 🔥", 0.92, 0.45),
